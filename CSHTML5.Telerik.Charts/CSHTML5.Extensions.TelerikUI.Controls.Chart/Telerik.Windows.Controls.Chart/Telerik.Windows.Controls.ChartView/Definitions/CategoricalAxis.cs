        //-------------------------------------//
        //-------------------------------------//
        //-------------------------------------//

        //-------------------------------------//
        //------------- METHODS ---------------//
        //-------------------------------------//
        public CategoricalAxis()
        {
            _majorTickInterval = 0;
        }
        //-------------------------------------//
        //-------------------------------------//
        //-------------------------------------//

    }
}
