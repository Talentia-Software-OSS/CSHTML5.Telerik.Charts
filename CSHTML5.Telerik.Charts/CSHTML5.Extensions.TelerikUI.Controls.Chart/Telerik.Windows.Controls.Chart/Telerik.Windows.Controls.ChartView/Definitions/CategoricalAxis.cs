namespace Telerik.Windows.Controls.ChartView
{
    public class CategoricalAxis : ChartAxis
    {
        //-------------------------------------//
        //-------------- FIELDS ---------------//
        //-------------------------------------//
        private int _majorTickInterval;
        //-------------------------------------//
        //-------------------------------------//
        //-------------------------------------//

        //-------------------------------------//
        //------------ PROPERTIES -------------//
        //-------------------------------------//
        public int MajorTickInterval
        {
            get { return _majorTickInterval; }
